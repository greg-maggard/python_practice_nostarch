cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse = True)
print(cars)

#You can also sort a list *temporarily*, without altering the original list. This just displays it in sorted form. 
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(sorted(cars))
print(cars)

#You can print a list in reverse order as well (note that this doesn't sort in reverse alphabetically, it just flips the original order)
#(This does change the list permanently, but if you want to go back, you can reverse the list again.)
print(cars)
cars.reverse()
print(cars)

#Finding length:
len(cars)

